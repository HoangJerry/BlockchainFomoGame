		var startIndex = (indexPage-1)*sizeOfView;
		var endIndex = parseInt(startIndex)+parseInt(sizeOfView);

		//for case content is empty
		if (content.length == 0) {
			startIndex = 0;
			endIndex = 0;
			pageQuantity = 0;
		}
		//for case endIndex not equal quantity*sizeOfView
		if (indexPage == pageQuantity) {
			endIndex = content.length;
		}
